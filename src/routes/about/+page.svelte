<PageHeader>What's this all about?</PageHeader>

<p>
	I sometimes read books containing data. Books about climate change with lots of numbers, for
	example. Later, I frequently find myself unable to remember those numbers. I may even misquote
	them. "Better write 'em down," I thought.
</p>

<p>
	But you know what happens then: pieces of paper everywhere. And never the one you're looking for.
</p>

<p>
	When I'm not reading books or napping, and not away touring on my bicycle, I like to write code.
	To develop websites. Like this one. To keep track of some of those numbers.
</p>

<p>I'm not an expert. In anything much. But neither do I eschew experts.</p>

<p>
	One lesson I have learned from various stints at university is to listen carefully to those who
	are smarter than I am and are better informed about their area of expertise than I do &ndash; and
	have spent, in many cases, decades honing that specialist knowledge. I could spend a lifetime on <a
		href="https://youtu.be"
		class="text-sky-600">YouTube</a
	> and still not be worthy to sit at their feet.
</p>

<p>
	When experts talk, I listen. But not blindly. Not all experts are created equally; some are
	&ndash; tragically &ndash; paid shills. I do investigate experts credentials, preferring
	peer-reviewed credible sources over discredited guests on inflammatory podcasts.
</p>

<p>
	If you find errors on this site, please mail me at
	<span class="font-mono">dave&ndash;at&ndash;dmorg.org</span>
	with the words "Numbers Error!" or equivalent in the subject. Include a link in your mail to a credible
	publication that refutes my assertion, to help me with any corrections
</p>

<p>
	This site is primarily for my own personal use and entertainment. But who knows? It might help you
	resolve a disputed point during lively discussion in the pub on a Tuesday night!
</p>

<p>
	Some feedback will help keep me focused on adding more content but this will never become a
	comprehensive resource: I am easily distracted &ndash; and I have too many other time-consuming
	rabbit-holes to explore. The content, such as it is, will reflect my own interests. And I do have
	some biases: I believe that the earth is not flat, that man did set foot on the moon, that
	vaccines save lives &ndash; and that Dunning-Kruger is everywhere.
</p>
<p>
	As this is primarily a hobby-site where I am experimenting somewhat with web-technologies, you may
	find the 'user-experience' (UX) a little different. Also, the units I use default to metric, or
	SI. If you come from Liberia, Myanmar or the United States of America, and require 'imperial'
	units, hover over any unit that is in bold text for the old-fashioned equivalent ;-)
</p>

<p>Cheers, Dave.</p>

